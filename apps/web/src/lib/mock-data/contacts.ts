export function getContactsByType(
	type: "tenant" | "agent" | "owner",
): Contact[] {
	return mockContacts.filter((c) => c.type === type);
}

export function getContactById(id: string): Contact | undefined {
	return mockContacts.find((c) => c.id === id);
}

export function searchContacts(
	query: string,
	type?: "tenant" | "agent" | "owner",
): Contact[] {
	const normalizedQuery = query.toLowerCase();
	return mockContacts.filter((contact) => {
		if (type && contact.type !== type) return false;
		return (
			contact.firstName?.toLowerCase().includes(normalizedQuery) ||
			contact.lastName?.toLowerCase().includes(normalizedQuery) ||
			contact.email?.toLowerCase().includes(normalizedQuery) ||
			contact.companyName?.toLowerCase().includes(normalizedQuery) ||
			contact.phone?.includes(query) ||
			contact.mobile?.includes(query)
		);
	});
}
